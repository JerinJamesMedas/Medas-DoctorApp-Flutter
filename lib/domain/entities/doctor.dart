class Doctor {
  final String docId;
  final String name;
  final String speciality;
  final String bio;
  final String phone;
  final String email;
  final String city;

  Doctor({
    required this.docId,
    required this.name,
    required this.speciality,
    required this.bio,
    required this.phone,
    required this.email,
    required this.city,
  });
}